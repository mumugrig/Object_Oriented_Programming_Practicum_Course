#pragma once

// интерфейс за команда, която има възможност за undo
// важно е в наследниците, които променят някакво състояние да пазим информацията необходима за да се върнем в предишното състояние
// работим с файлови дървета, така че искаме да избягваме копиране на директориите доколкото е възможно

class Command{
public:
    virtual void execute() = 0;
    virtual void undo() = 0;
    virtual ~Command() = default;
};